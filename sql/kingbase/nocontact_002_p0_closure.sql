UPDATE nc_warning_message
SET message_status = 'pending'
WHERE message_status in ('sent', 'read');

UPDATE nc_warning_message
SET message_status = 'closed'
WHERE message_status = 'handled';

UPDATE nc_warning_message
SET message_status = 'ignored'
WHERE message_status = 'archived';

UPDATE nc_warning_message
SET latest_hit_time = trigger_time
WHERE latest_hit_time IS NULL;

UPDATE nc_warning_message
SET hit_count = 1
WHERE hit_count IS NULL;

UPDATE nc_warning_message
SET business_key = rule_id || ':' || indicator_id || ':' || coalesce(responsible_unit_id, 0) || ':' || coalesce(period_key, '')
WHERE business_key IS NULL OR business_key = '';

