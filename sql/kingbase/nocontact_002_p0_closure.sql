-- 营商无感业务模块 002：数据融合与监测预警 P0 闭环增量
-- 手工执行，幂等，不由服务启动自动迁移。

CREATE TABLE IF NOT EXISTS nc_fusion_collection_batch (
  batch_id bigint not null GENERATED BY DEFAULT AS IDENTITY,
  task_id bigint,
  batch_name varchar(160) not null,
  source_type varchar(24) default 'form',
  source_name varchar(160) default '',
  source_record_id varchar(120) default '',
  responsible_unit_id bigint,
  responsible_unit_name varchar(160) default '',
  region_code varchar(32) default '',
  region_name varchar(64) default '',
  period_type varchar(24) default 'month',
  period_year int,
  period_quarter int,
  period_month int,
  period_key varchar(32) not null,
  batch_status varchar(24) default 'draft',
  submit_by varchar(64) default '',
  submit_time timestamp,
  audit_by varchar(64) default '',
  audit_time timestamp,
  audit_opinion varchar(500) default '',
  item_count int default 0,
  del_flag char(1) default '0',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500) default null,
  CONSTRAINT pk_nc_fusion_collection_batch PRIMARY KEY (batch_id)
);

COMMENT ON TABLE nc_fusion_collection_batch IS '数据采集批次';
COMMENT ON COLUMN nc_fusion_collection_batch.batch_status IS '状态（draft草稿 pending_audit待审核 approved已审核 rejected已驳回）';

CREATE TABLE IF NOT EXISTS nc_fusion_collection_item (
  item_id bigint not null GENERATED BY DEFAULT AS IDENTITY,
  batch_id bigint not null,
  indicator_id bigint not null,
  indicator_name varchar(200) default '',
  indicator_code varchar(80) default '',
  raw_value varchar(500) default '',
  value_decimal numeric(18,4),
  value_text varchar(500) default '',
  value_date timestamp,
  validation_status varchar(24) default 'valid',
  validation_message varchar(500) default '',
  is_current char(1) default '1',
  del_flag char(1) default '0',
  create_by varchar(64) default '',
  create_time timestamp,
  update_by varchar(64) default '',
  update_time timestamp,
  remark varchar(500) default null,
  CONSTRAINT pk_nc_fusion_collection_item PRIMARY KEY (item_id)
);

COMMENT ON TABLE nc_fusion_collection_item IS '数据采集明细';
COMMENT ON COLUMN nc_fusion_collection_item.validation_status IS '校验状态（valid有效 invalid无效）';

CREATE TABLE IF NOT EXISTS nc_fusion_collection_audit_log (
  log_id bigint not null GENERATED BY DEFAULT AS IDENTITY,
  batch_id bigint not null,
  from_status varchar(24) default '',
  to_status varchar(24) default '',
  action_name varchar(80) default '',
  audit_by varchar(64) default '',
  audit_time timestamp,
  audit_opinion varchar(500) default '',
  create_by varchar(64) default '',
  create_time timestamp,
  remark varchar(500) default null,
  CONSTRAINT pk_nc_fusion_collection_audit_log PRIMARY KEY (log_id)
);

COMMENT ON TABLE nc_fusion_collection_audit_log IS '数据采集审核日志';

CREATE TABLE IF NOT EXISTS nc_warning_message_handle_log (
  log_id bigint not null GENERATED BY DEFAULT AS IDENTITY,
  message_id bigint not null,
  from_status varchar(24) default '',
  to_status varchar(24) default '',
  handle_by varchar(64) default '',
  handle_time timestamp,
  handle_opinion varchar(500) default '',
  create_by varchar(64) default '',
  create_time timestamp,
  remark varchar(500) default null,
  CONSTRAINT pk_nc_warning_message_handle_log PRIMARY KEY (log_id)
);

COMMENT ON TABLE nc_warning_message_handle_log IS '预警消息处理日志';

